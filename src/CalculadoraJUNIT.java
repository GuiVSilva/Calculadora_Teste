public class CalculadoraJUNIT {
    //Operacoes matematicas

    //Metodo de soma
    public int soma(int a, int b){
        return a + b;
    }

    //Metodo de subtracao
    public int subtracao(int a, int b){
        return a - b;
    }

    //Metodo de divisao
    public int divisao(int a, int b){
        return a / b;
    }

    //Metodo de multiplicacao
    public int multiplicacao(int a, int b){
        return a * b;
    }
}
